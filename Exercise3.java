import java.util.Scanner;

public class Exercise3{
	public static void main(String[] args){
	
	Scanner input = new Scanner(System.in);
	
	System.out.print("Enter any magic number  ");
		int magicNumber = input.nextInt();
		
	int givenNumber = 100;
	int square = magicNumber * magicNumber;
	
	if(magicNumber > 100);
	System.out.printf("%d > %d%n", magicNumber, givenNumber);
	if(magicNumber == 100);
	System.out.printf("%d == %d%n", magicNumber, givenNumber);
	if(magicNumber != 100);
	System.out.printf("%d != %d%n", magicNumber, givenNumber);
	if(magicNumber < 100);
	System.out.printf("%d < %d%n", magicNumber, givenNumber);
	
	if(square > 100);
	System.out.printf("%d > %d%n", square, givenNumber);
	if(square > 100);
	System.out.printf("%d == %d%n", square, givenNumber);
	if(square > 100);
	System.out.printf("%d != %d%n", square, givenNumber);
	if(square > 100);
	System.out.printf("%d < %d%n", square, givenNumber);
	
		
	
	
	}

}
